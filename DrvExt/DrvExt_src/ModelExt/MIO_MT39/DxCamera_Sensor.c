void SPI_RX_W(UINT32 address,UINT32 value)
{

  address = address | 0x80 ;
  
	spi_setCSActive(SPI_ID_2, TRUE);     //CS LOW
	
	spi_setTransferLen(SPI_ID_2, SPI_TRANSFER_LEN_1BYTE);//set transfer data lenth
	spi_writeSingle(SPI_ID_2, address);
	spi_writeSingle(SPI_ID_2, value);
	
	spi_setCSActive(SPI_ID_2, FALSE);  ////CS HI
	//DBG_ERR("SPI read = %x , %x delay 1us \r\n",address,value);
	
}
