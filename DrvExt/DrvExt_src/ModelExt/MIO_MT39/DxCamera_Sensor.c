UINT32 SPI_RX_R(UINT32 address,UINT32 bit_number)
{	
	UINT32  data  = 0;
	
  spi_setCSActive(SPI_ID_2, TRUE);
  
	spi_setTransferLen(SPI_ID_2, SPI_TRANSFER_LEN_1BYTE);
	spi_writeSingle(SPI_ID_2, address);
	spi_readSingle(SPI_ID_2, &data );

	spi_setCSActive(SPI_ID_2, FALSE);
	
	//DBG_ERR("SPI read = %x delay 1us \r\n",data);
	return data ;
}
