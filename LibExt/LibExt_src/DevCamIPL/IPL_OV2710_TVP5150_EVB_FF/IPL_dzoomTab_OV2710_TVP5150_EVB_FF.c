UINT32* SenMode2Tbl(UINT32 Id, UINT32 SenMode, UINT32 *DzMaxidx)
{
    UINT32 *Ptr = NULL;

    if (Id == IPL_ID_1)
    {
        switch(SenMode)
        {
            case SENSOR_MODE_1:
                Ptr = (UINT32*)&VDOZOOM_INFOR_MODE_1_TABLE[0][0];
                *DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_1_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
                break;

            case SENSOR_MODE_2:
                Ptr = (UINT32*)&VDOZOOM_INFOR_MODE_2_TABLE[0][0];
                *DzMaxidx = (sizeof(VDOZOOM_INFOR_MODE_2_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
                break;
            default:
                Ptr = NULL;
                *DzMaxidx = 0;
                break;
        }
    }
    else if (Id == IPL_ID_2)
    {
        switch(SenMode)
        {
            case SENSOR_MODE_2:
            case SENSOR_MODE_1:
                Ptr = (UINT32*)&VDOZOOM_INFOR_TVP5150_MODE_1_TABLE[0][0];
                *DzMaxidx = (sizeof(VDOZOOM_INFOR_TVP5150_MODE_1_TABLE) / 4 / DZOOM_ITEM_MAX) - 1;
                break;

            default:
                Ptr = NULL;
                *DzMaxidx = 0;
                break;
        }
    }

    return Ptr;
}
